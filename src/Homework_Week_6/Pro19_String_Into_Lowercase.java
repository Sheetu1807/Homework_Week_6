package Homework_Week_6;

public class Pro19_String_Into_Lowercase {

    // Input: THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG
    // Output: the quick brown fox jumps over the lazy dog


    public static void main(String[] args) {
        String line = "THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG"; // local variable

        System.out.println("Convert string into lowercase:" + line.toLowerCase());

    }
}
